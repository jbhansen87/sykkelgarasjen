window.getServiceBySlug = function getServiceBySlug(slug) {
  for (const category of window.SERVICE_CATEGORIES) {
    for (const item of category.items) {
      if (item.slug === slug) {
        return { ...item, category: category.title };
      }
    }
  }

  return null;
};
